'''
A number is entered by user, check whether number is positive or negative number or not




             ---------------------
            -3  -2  -1  0  1  2  3

Positive: number must be greater than zero
negative: number must be less than zero

'''

print("Enter number:")
n=int(input())

if n>0:
    print("Number is positive")
else:
    print("Number is negative")

